package com.sap.ttt;

public class Player {
    private String name;
    private String symbol;

    // constructor
    public Player(String name, String symbol) {
        this.name = name;
        this.symbol = symbol;

    }


    // getters
    public String getName() {
        return name;
    }

    public String getSymbol() {
        return symbol;

    }
}

